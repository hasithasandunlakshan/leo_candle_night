"use client"
import { motion, useTransform, useScroll } from "framer-motion";
import { useRef } from "react";
import { ItemList } from "./ItemList";

const Example = () => {
  return (
    <div className=" h-full">
     <ItemList/>
      <HorizontalScrollCarousel />
      
    
    </div>
  );
};

const HorizontalScrollCarousel = () => {
  const targetRef = useRef(null);
  const { scrollYProgress } = useScroll({
    target: targetRef,
  });

  const x = useTransform(scrollYProgress, [0, 2], ["1%", "-105%"]);

  return (
    <section ref={targetRef} className="relative h-[55rem] ">
      <div className="sticky top-0 flex h-screen items-center will-change-scroll overflow-y-auto overflow-x-hidden">
        <motion.div style={{ x }} className="flex gap-4">
          {cards.map((card) => {
            return <Card card={card} key={card.id} />;
          })}
        </motion.div>
      </div>
    </section>
  );
};

const Card = ({ card }) => {
  return (
    <div
      key={card.id}
      className="group relative  h-72 w-96 overflow-hidden bg-neutral-200"
    >
      <div
        style={{
          backgroundImage: `url(${card.url})`,
          backgroundSize: "cover",
          backgroundPosition: "center",
        }}
        className="absolute inset-0 z-0 transition-transform duration-300 group-hover:scale-110"
      ></div>
      <div className="absolute inset-0 z-10 grid place-content-center">
        <p className="bg-gradient-to-br from-white/20 to-white/0 p-8 text-6xl font-black uppercase text-white backdrop-blur-lg">
          {card.title}
        </p>
      </div>
    </div>
  );
};

export default Example;

